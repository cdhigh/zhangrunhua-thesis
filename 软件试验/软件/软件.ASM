;说明用Timer0进行延时的实验			
	LIST	P=16F716	
	INCLUDE	P16F716.INC	
Cnt0	EQU	0X20	;延时计数器
Cnt1	EQU	0X21	;延时计数器
	ORG	0X00	;程序从0X00处开始
	BANKSEL	TRISA	;BANK1
	MOVLW	B'00000000'	;设置端口A方向
	MOVWF	TRISA	
	MOVLW	B'00000000'	;设置端口B方向
	MOVWF	TRISB	
	BCF	STATUS,RP0	;BANK0
	CLRF	PORTA	
	CLRF	PORTB	
	MOVLW	0X01	
	CALL	TABLE1	
	MOVWF	PORTB	
			
TABLE1	ADDWF	PCL,1	
	RETLW	B'11111100'	;0
	RETLW	B'01000100'	;1
	RETLW	B'10110110'	;2
	RETLW	B'01110110'	;3
	RETLW	B'01001110'	;4
	RETLW	B'01111010'	;5
	RETLW	B'11111010'	;6
	RETLW	B'01010100'	;7
	RETLW	B'11111110'	;8
	RETLW	B'01111110'	;9
			
	END		
